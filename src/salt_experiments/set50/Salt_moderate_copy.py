def _read_grayscale_float255(path, resize_shape=None):
    img = skimage.io.imread(path)

    if img.ndim == 4:
        img = img[0]

    if img.ndim == 3 and img.shape[-1] == 4:
        img = img[..., :3]

    if img.ndim == 3 and img.shape[-1] == 3:
        img = skimage.color.rgb2gray(img)

    if img.dtype.kind == 'f':
        img = (np.clip(img, 0, 1) * 255).astype(np.float32)
    else:
        img = img.astype(np.float32)

    if resize_shape is not None:
        img = resize(
            img,
            resize_shape,
            preserve_range=True,
            anti_aliasing=True
        ).astype(np.float32)

    return np.clip(img, 0, 255).astype(np.float32)


def _print_image_metrics(results):
    print(f"\nImage: {results['file_name']}")
    print(f"  Noisy  PSNR={results['psnr_noisy']:.4f} SSIM={results['ssim_noisy']:.4f}")
    print(f"  NLM    PSNR={results['psnr_nlm']:.4f} SSIM={results['ssim_nlm']:.4f}")
    print(f"  GEONLM PSNR={results['psnr_gnlm']:.4f} SSIM={results['ssim_gnlm']:.4f}")
    print(f"  Median PSNR={results['psnr_median']:.4f} SSIM={results['ssim_median']:.4f}")
    print(f"  ASWMF  PSNR={results['psnr_aswmf']:.4f} SSIM={results['ssim_aswmf']:.4f}")


def _call_with_optional_stdout(func, verbose, *args, **kwargs):
    if verbose:
        return func(*args, **kwargs)

    with contextlib.redirect_stdout(io.StringIO()):
        return func(*args, **kwargs)


def _score(psnr, ssim):
    return 0.5 * psnr + 0.5 * (ssim * 100)


def _metrics(reference_uint8, filtered_uint8):
    psnr = peak_signal_noise_ratio(reference_uint8, filtered_uint8, data_range=255)
    ssim = structural_similarity(reference_uint8, filtered_uint8, data_range=255)
    return psnr, ssim, _score(psnr, ssim)


def _read_filtered_or_none(directory, file_name):
    path = Path(directory) / file_name
    if not path.exists():
        return None

    return _read_grayscale_float255(path).astype(np.uint8)


def _save_uint8_image(directory, file_name, image):
    path = Path(directory) / file_name
    path.parent.mkdir(parents=True, exist_ok=True)
    skimage.io.imsave(str(path), np.clip(image, 0, 255).astype(np.uint8))


def _normalize_target_file_names(file_names):
    if file_names is None:
        return None

    normalized = set()
    for file_name in file_names:
        path = Path(str(file_name))
        normalized.add(path.name)
        normalized.add(path.stem)

    return normalized


def _is_target_file(file_name, target_file_names):
    if target_file_names is None:
        return True

    path = Path(file_name)
    return path.name in target_file_names or path.stem in target_file_names


def generate_salt_experiment_moderate(parameters):
    """
    Run the moderate-noise salt-and-pepper denoising experiment using NLM, GEO-NLM,
    median filtering, and ASWMF.
    
    The function:
      1. Loads the existing NLM pickle generated by the first phase.
      2. Reuses already saved filtered images to compute PSNR, SSIM, and score.
      3. Runs only the missing GEO-NLM, median, or ASWMF outputs.
      4. Saves the complete metrics table to pickle + XLSX.
    """

    # Unpack configuration parameters
    root_dir_output_moderate = parameters['root_dir_output_moderate']
    dir_out_nlm = parameters['dir_out_nlm']
    dir_out_geonlm = parameters['dir_out_geonlm']
    dir_out_median = parameters['dir_out_median']
    dir_out_aswmf = parameters['dir_out_aswmf']
    dir_out_results = parameters['dir_out_results']
    name_pickle_nlm_output_moderate = parameters['name_pickle_nlm_output_moderate']
    name_pickle_results_gnlm_median_aswmf_output_moderate = parameters['name_pickle_results_gnlm_median_aswmf_output_moderate']
    name_results_xlsx_nlm_gnlm_median_aswmf_output_moderate = parameters['name_results_xlsx_nlm_gnlm_median_aswmf_output_moderate']

    f = parameters['f']          # Patch radius for GEO-NLM
    t = parameters['t']          # Search window radius for GEO-NLM
    nn = parameters['nn']        # Number of nearest neighbors / similar patches in GEO-NLM

    median_size = parameters.get('median_size', 3)
    aswmf_radius = parameters.get('aswmf_radius', 3)
    aswmf_weight_diag_1 = parameters.get('aswmf_weight_diag_1', 1.0)
    aswmf_weight_diag_2 = parameters.get('aswmf_weight_diag_2', 1.0)
    aswmf_weight_other = parameters.get('aswmf_weight_other', 10.0)
    resize_shape = parameters.get('resize_shape')
    print_metrics = parameters.get('print_metrics', False)
    verbose_internal = parameters.get('verbose_internal', False)
    target_file_names = _normalize_target_file_names(
        parameters.get(
            'target_file_names',
            ['45.gif', '46.gif', '47.gif', '48.gif', '49.gif',
             '5.gif', '6.gif', '7.gif', '8.gif', '9.gif']
        )
    )

    for directory in [
        root_dir_output_moderate,
        dir_out_nlm,
        dir_out_geonlm,
        dir_out_median,
        dir_out_aswmf,
        dir_out_results,
    ]:
        Path(directory).mkdir(parents=True, exist_ok=True)

    array_gnlm_median_aswmf_moderate_filtereds = []

    # Read the existing NLM results instead of recomputing the expensive first phase.
    vector = load_pickle(dir_out_results, name_pickle_nlm_output_moderate)

    for array in vector:

        # Retrieve data saved during the NLM phase
        img = array['img_reference_np']
        img_noisy_salt_pepper_np = array['img_noisy_salt_pepper_np']
        nlm_h = array['nlm_h']
        file_name = array['file_name']
        should_run_file = _is_target_file(file_name, target_file_names)
        estimated_sigma_salt_pepper = array['estimated_sigma_salt_pepper']
        salt_pepper_density = array['salt_pepper_density']
        salt_prob = array['salt_prob']
        pepper_prob = array['pepper_prob']

        img_reference_uint8 = np.clip(img, 0, 255).astype(np.uint8)
        img_noisy_uint8 = np.clip(img_noisy_salt_pepper_np, 0, 255).astype(np.uint8)

        img_filtered_nlm_uint8 = _read_filtered_or_none(dir_out_nlm, file_name)
        if img_filtered_nlm_uint8 is None:
            img_filtered_nlm_uint8 = np.clip(array['img_filtered_nlm'], 0, 255).astype(np.uint8)
            _save_uint8_image(dir_out_nlm, file_name, img_filtered_nlm_uint8)

        psnr_noisy, ssim_noisy, _ = _metrics(img_reference_uint8, img_noisy_uint8)
        psnr_nlm, ssim_nlm, score_nlm = _metrics(img_reference_uint8, img_filtered_nlm_uint8)

        # ------------------
        # GEO-NLM DENOISING
        # ------------------

        # Select multiplier based on NLM h and estimated salt-and-pepper noise level
        mult = get_multiplier(nlm_h, estimated_sigma_salt_pepper)
        h_gnlm = nlm_h * mult
        img_filtered_gnlm_uint8 = _read_filtered_or_none(dir_out_geonlm, file_name)

        if img_filtered_gnlm_uint8 is None and should_run_file:
            ini = time.time()
            img_filtered_gnlm_uint8, h_gnlm, _, _, _ = _call_with_optional_stdout(
                run_geonlm_pipeline,
                verbose_internal,
                img,
                nlm_h,
                img_noisy_salt_pepper_np,
                f,
                t,
                mult,
                nn
            )
            time_geonlm = time.time() - ini
            _save_uint8_image(dir_out_geonlm, file_name, img_filtered_gnlm_uint8)
        else:
            time_geonlm = np.nan

        if img_filtered_gnlm_uint8 is None:
            raise FileNotFoundError(
                f'Missing GEO-NLM output for {file_name}; add it to target_file_names to run it.'
            )

        psnr_gnlm, ssim_gnlm, score_gnlm = _metrics(
            img_reference_uint8,
            img_filtered_gnlm_uint8
        )

        # ------------
        # MEDIAN STEP
        # ------------

        img_filtered_median_uint8 = _read_filtered_or_none(dir_out_median, file_name)
        if img_filtered_median_uint8 is None and should_run_file:
            ini = time.time()
            img_filtered_median = median_filter(
                img_noisy_salt_pepper_np,
                size=median_size,
                mode='reflect'
            )
            img_filtered_median_uint8 = np.clip(
                img_filtered_median, 0, 255
            ).astype(np.uint8)
            time_median = time.time() - ini
            _save_uint8_image(dir_out_median, file_name, img_filtered_median_uint8)
        else:
            time_median = np.nan

        if img_filtered_median_uint8 is None:
            raise FileNotFoundError(
                f'Missing Median output for {file_name}; add it to target_file_names to run it.'
            )

        psnr_median, ssim_median, score_median = _metrics(
            img_reference_uint8,
            img_filtered_median_uint8
        )

        # ----------
        # ASWMF STEP
        # ----------

        img_filtered_aswmf_uint8 = _read_filtered_or_none(dir_out_aswmf, file_name)
        if img_filtered_aswmf_uint8 is None and should_run_file:
            ini = time.time()
            img_filtered_aswmf = aswmf_filter(
                img_noisy_salt_pepper_np.astype(np.float32),
                radius=aswmf_radius,
                weight_diag_1=aswmf_weight_diag_1,
                weight_diag_2=aswmf_weight_diag_2,
                weight_other=aswmf_weight_other
            )
            img_filtered_aswmf_uint8 = np.clip(
                img_filtered_aswmf, 0, 255
            ).astype(np.uint8)
            time_aswmf = time.time() - ini
            _save_uint8_image(dir_out_aswmf, file_name, img_filtered_aswmf_uint8)
        else:
            time_aswmf = np.nan

        if img_filtered_aswmf_uint8 is None:
            raise FileNotFoundError(
                f'Missing ASWMF output for {file_name}; add it to target_file_names to run it.'
            )

        psnr_aswmf, ssim_aswmf, score_aswmf = _metrics(
            img_reference_uint8,
            img_filtered_aswmf_uint8
        )

        # Collect all metrics for this image
        dict_results = {
            'nlm_h': nlm_h,
            'h_gnlm': h_gnlm,
            'estimated_sigma_salt_pepper': estimated_sigma_salt_pepper,
            'salt_pepper_density': salt_pepper_density,
            'salt_prob': salt_prob,
            'pepper_prob': pepper_prob,
            'median_size': median_size,
            'aswmf_radius': aswmf_radius,
            'aswmf_weight_diag_1': aswmf_weight_diag_1,
            'aswmf_weight_diag_2': aswmf_weight_diag_2,
            'aswmf_weight_other': aswmf_weight_other,
            'resize_shape': resize_shape,

            'ssim_noisy': ssim_noisy,
            'ssim_nlm': ssim_nlm,
            'ssim_gnlm': ssim_gnlm,
            'ssim_median': ssim_median,
            'ssim_aswmf': ssim_aswmf,

            'psnr_noisy': psnr_noisy,
            'psnr_nlm': psnr_nlm,
            'psnr_gnlm': psnr_gnlm,
            'psnr_median': psnr_median,
            'psnr_aswmf': psnr_aswmf,

            'score_nlm': score_nlm,
            'score_gnlm': score_gnlm,
            'score_median': score_median,
            'score_aswmf': score_aswmf,
            'time_geonlm': time_geonlm,
            'time_median': time_median,
            'time_aswmf': time_aswmf,
            'nlm_loaded_from_disk': True,
            'geonlm_loaded_from_disk': np.isnan(time_geonlm),
            'median_loaded_from_disk': np.isnan(time_median),
            'aswmf_loaded_from_disk': np.isnan(time_aswmf),

            'file_name': file_name,
        }

        if print_metrics:
            _print_image_metrics(dict_results)

        array_gnlm_median_aswmf_moderate_filtereds.append(dict_results)

    # Save the combined GEO-NLM, median, and ASWMF results to pickle
    save_pickle(
        array_gnlm_median_aswmf_moderate_filtereds,
        dir_out_results,
        name_pickle_results_gnlm_median_aswmf_output_moderate
    )

    # Export all results to an XLSX spreadsheet
    save_results_to_xlsx(
        array_gnlm_median_aswmf_moderate_filtereds,
        dir_out_results,
        name_results_xlsx_nlm_gnlm_median_aswmf_output_moderate
    )
